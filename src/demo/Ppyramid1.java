package demo;

public class Ppyramid1 {

	public static void main(String[] args) {

		for(int i=1;i<=8;i++) {
			for(int j=1;j<=8-i;j++)
			{
				System.out.print(" ");
			}
		for(int k=1;k<=i;k++)
		{
			System.out.print(" *");
		}

	System.out.println(); 

	}
		
		
		for(int i=1;i<=8;i++) {
			for(int j=0;j<i;j++)
			{
				System.out.print(" ");
			}
		for(int k=i;k<=8;k++)
		{
			System.out.print(" *");
		}
	
	System.out.println(); 

	}


	
}









}