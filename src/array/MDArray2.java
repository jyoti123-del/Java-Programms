package array;

public class MDArray2 {

	public static void main(String[] args) {
		// without limit
		int a[][]= {{10,20},{30,40},{40,50},{60,70},{80,90},{90,10},{85,95}};
		for(int i=0;i<=6;i++) {
			for(int j=0;j<=1;j++) {
				System.out.print(a[i][j]+" ");}
				
			System.out.println();
			
		}

	}

}
