package Oopsconcept;

public class Inheritance1 {
	int a=10;
	static int b=20;
	protected static int c=30;
	protected int d=40;
	public static int e=50;
	public  int f=60;
	private int g=70;
	private static int h=80;
	
	public static void main(String[] args) {
	int a=100; int b=200;int c=300;int d=400;
	int e=500;int f=600;int g=700;int h=800;
		Inheritance1 x=new Inheritance1();
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		System.out.println(x.a);
		System.out.println(Inheritance1.b);
		System.out.println(Inheritance1.c);
		System.out.println(x.d);
		System.out.println(Inheritance1.e);
		System.out.println(x.f);
		System.out.println(x.g);
		System.out.println(Inheritance1.h);
		;
		

	}

}
